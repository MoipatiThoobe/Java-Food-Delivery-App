
package task20;
/**
 * This class stores the information about the customer
 * @author Moipati Thoobe
 * @version 3.0
 */

public class Customer {
	/**
	 * String value to store customer name
	 */
	String custName;
	/**
	 * String value to store customer phone number
	 */
	String custNum;
	/**
	 * String value to store customer address
	 */
	String custAddress;
	/**
	 * String value to store customer location
	 */
	String custLocation;
	/**
	 * String value to store customer email
	 */
	String custEmail;
	
	
	
	/**
	 * method to get the customer name
	 * @return returns customer name
	 */
	public String getCustName() {
		return custName;
	}
	
	/**
	 * method to set the customer name
	 * @param custName customer name value
	 */
	public void setCustName(String custName) {
		this.custName = custName;
	}
	
	/**
	 * method to get the customer number
	 * @return returns the customer number
	 */
	public String getCustNum() {
		return custNum;
	}
	
	/**
	 * method to set the customer number
	 * @param custNum customer number value
	 */
	public void setCustNum(String custNum) {
		this.custNum = custNum;
	}
	
	/**
	 * method to get the customer address
	 * @return returns the customer address
	 */
	public String getCustAddress() {
		return custAddress;
	}
	
	/**
	 * method to set the customer address
	 * @param custAddress customer address value
	 */
	public void setCustAddress(String custAddress) {
		this.custAddress = custAddress;
	}
	
	/**
	 * method to get the customer location
	 * @return returns the customer location
	 */
	public String getCustLocation() {
		return custLocation;
	}
	
	/**
	 * method to set the customer location
	 * @param custLocation customer location value
	 */
	public void setCustLocation(String custLocation) {
		this.custLocation = custLocation;
	}
	
	/**
	 * method to get the customer email
	 * @return returns the customer email address
	 */
	public String getCustEmail() {
		return custEmail;
	}
	
	/**
	 * method to set the customer email
	 * @param custEmail customer email value
	 */
	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}
	

	
	

}
